export class Film{
    nombre : string;
    duracion : string;
    pais : string;
    genero : string;
    rating : string;
    constructor(nombre:string, duracion:string, pais:string, genero:string,rating:string){
        this.nombre = nombre;
        this.duracion = duracion;
        this.pais = pais;
        this.genero = genero;
        this.rating = rating;

    }
}
















